// Timing: 1s fade-in + 35s painting + 30s freeze. One painter per activation.
const TRANSITION_MS = 1000;
const PAINTING_DURATION_MS = 35000;
const FREEZE_MS = 30000;

// Pre-allocated reusable style objects
const pixelStyle = { fill: 0x000000 as number, alpha: 1 };

function shuffleOrder(arr: number[]): void {
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    const tmp = arr[i];
    arr[i] = arr[j];
    arr[j] = tmp;
  }
}

function drawPixelPainter(
  api: ActorUpdateAPI,
  x: number,
  y: number,
  painter: PainterDef,
  armAngle: number,
  ps: number,
  alpha: number
): void {
  // Hat
  api.brush.rect(x - ps, y - ps * 3, ps * 2, ps, {
    fill: painter.hatColor, alpha: 0.95 * alpha,
  });
  // Head
  api.brush.rect(x - ps * 0.5, y - ps * 2, ps, ps, {
    fill: painter.skinColor, alpha: 0.95 * alpha,
  });
  // Body
  api.brush.rect(x - ps * 0.75, y - ps, ps * 1.5, ps * 1.5, {
    fill: painter.shirtColor, alpha: 0.95 * alpha,
  });
  // Arm with brush
  const armLen = ps * 1.5;
  const armEndX = x + ps + Math.cos(armAngle) * armLen;
  const armEndY = y - ps * 0.5 + Math.sin(armAngle) * armLen * 0.3;
  api.brush.line(x + ps * 0.5, y - ps * 0.5, armEndX, armEndY, {
    color: painter.skinColor, width: Math.max(2.5, ps * 0.3), alpha: 0.9 * alpha,
  });
  // Brush tip
  api.brush.circle(armEndX, armEndY, Math.max(2, ps * 0.25), {
    fill: 0xffffff, alpha: 0.8 * alpha,
  });
  // Legs
  api.brush.rect(x - ps * 0.4, y + ps * 0.5, ps * 0.3, ps, {
    fill: 0x222222, alpha: 0.9 * alpha,
  });
  api.brush.rect(x + ps * 0.1, y + ps * 0.5, ps * 0.3, ps, {
    fill: 0x222222, alpha: 0.9 * alpha,
  });
}

const actor: Actor = {
  metadata,

  async setup(api: ActorSetupAPI): Promise<void> {
    const size = api.canvas.getSize();
    canvasW = size.width;
    canvasH = size.height;

    paintOrder = new Array(MAX_PIXELS);
    for (let i = 0; i < MAX_PIXELS; i++) {
      paintOrder[i] = i;
    }
    shuffleOrder(paintOrder);
    currentPainterIdx = -1;
  },

  update(api: ActorUpdateAPI, frame: FrameContext): void {
    const t = frame.time;

    // Pick a random painter once on first frame
    if (currentPainterIdx === -1) {
      currentPainterIdx = Math.floor(Math.random() * PAINTERS.length);
      shuffleOrder(paintOrder);
    }

    const painter = PAINTERS[currentPainterIdx];

    // Two phases: fade-in → painting → freeze forever
    let transAlpha: number;
    let paintProgress: number;
    let showLabel: boolean;
    let isFrozen = false;

    if (t < TRANSITION_MS) {
      // Phase 1: fade in
      transAlpha = t / TRANSITION_MS;
      paintProgress = 0;
      showLabel = false;
    } else if (t < TRANSITION_MS + PAINTING_DURATION_MS) {
      // Phase 2: painting pixels
      transAlpha = 1;
      const paintTime = t - TRANSITION_MS;
      paintProgress = Math.min(1, paintTime / PAINTING_DURATION_MS);
      showLabel = false;
    } else {
      // Phase 3: freeze forever — painting complete, show name
      transAlpha = 1;
      paintProgress = 1;
      showLabel = true;
      isFrozen = true;
    }

    // Painting area
    const margin = 16;
    const availW = canvasW - margin * 2;
    const availH = canvasH * 0.55;
    const pixSize = Math.min(
      Math.floor(availW / GRID_W),
      Math.floor(availH / GRID_H)
    );

    const paintW = pixSize * GRID_W;
    const paintH = pixSize * GRID_H;
    const paintX = (canvasW - paintW) / 2;
    const paintY = canvasH * 0.08;

    // Easel frame
    api.brush.rect(paintX - 4, paintY - 4, paintW + 8, paintH + 8, {
      fill: 0x5a3a1a, alpha: 0.8 * transAlpha,
    });
    api.brush.rect(paintX - 2, paintY - 2, paintW + 4, paintH + 4, {
      fill: 0x8b6914, alpha: 0.7 * transAlpha,
    });
    // Canvas bg
    api.brush.rect(paintX, paintY, paintW, paintH, {
      fill: 0xf5f0e0, alpha: 0.9 * transAlpha,
    });

    // Easel legs
    const easelLegW = Math.max(3, pixSize * 0.3);
    api.brush.line(
      paintX + paintW * 0.25, paintY + paintH + 4,
      paintX + paintW * 0.15, paintY + paintH + pixSize * 3,
      { color: 0x5a3a1a, width: easelLegW, alpha: 0.7 * transAlpha }
    );
    api.brush.line(
      paintX + paintW * 0.75, paintY + paintH + 4,
      paintX + paintW * 0.85, paintY + paintH + pixSize * 3,
      { color: 0x5a3a1a, width: easelLegW, alpha: 0.7 * transAlpha }
    );
    api.brush.line(
      paintX + paintW * 0.5, paintY + paintH + 4,
      paintX + paintW * 0.5, paintY + paintH + pixSize * 3.5,
      { color: 0x5a3a1a, width: easelLegW, alpha: 0.5 * transAlpha }
    );

    // Reveal pixels
    const numRevealed = Math.floor(paintProgress * MAX_PIXELS);
    for (let i = 0; i < numRevealed; i++) {
      const idx = paintOrder[i];
      const gx = idx % GRID_W;
      const gy = Math.floor(idx / GRID_W);

      pixelStyle.fill = painter.pixels[idx];
      pixelStyle.alpha = transAlpha;
      api.brush.rect(
        paintX + gx * pixSize,
        paintY + gy * pixSize,
        pixSize,
        pixSize,
        pixelStyle
      );
    }

    // Painter character
    if (transAlpha > 0.1) {
      // Arm stops moving when frozen (rests at side)
      const armAngle = isFrozen ? -0.8 : Math.sin(t * 0.004) * 0.6 - 0.3;
      const painterX = paintX + paintW * 0.8;
      const painterY = paintY + paintH + pixSize * 2.5;
      drawPixelPainter(api, painterX, painterY, painter, armAngle, pixSize, transAlpha);
    }

    // Label — shown during freeze phase
    if (showLabel) {
      const labelY = paintY + paintH + pixSize * 5;
      const labelText = `${painter.name} - "${painter.paintingName}"`;
      api.brush.text(labelText, canvasW / 2, labelY, {
        fontSize: 16,
        font: 'sans-serif',
        fill: 0xdddddd,
        alpha: 0.9,
        align: 'center',
        letterSpacing: 2,
      });
    }
  },

  async teardown(): Promise<void> {
    paintOrder = [];
    currentPainterIdx = -1;
    canvasW = 0;
    canvasH = 0;
  },
};

registerActor(actor);
export default actor;
